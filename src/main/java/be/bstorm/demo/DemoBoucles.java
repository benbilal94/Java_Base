package be.bstorm.demo;

public class DemoBoucles {
    public static void main(String[] args) {
        int count = 0;

        // Boucle WHILE

        while (count < 10){
            System.out.println("coucou c'est oui oui");
            count++;
        }

        // Boucle FOR

        for (int i = 0; i < 10; i++){
            System.out.println(i);
        }
    }
}
